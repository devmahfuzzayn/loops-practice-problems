var marks = 85;
var userName = 'my-self';

if (marks >= 80) {
    console.log(userName + " got A grade");
} else if (marks >= 60) {
    console.log(userName + " got B grade");
} else if (marks >= 50) {
    console.log(userName + " got C grade");
} else if (marks >= 40) {
    console.log(userName + " got D grade");
} else {
    console.log(userName + " got F grade");
}
